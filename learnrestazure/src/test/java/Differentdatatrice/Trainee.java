package Differentdatatrice;

public class Trainee {

    private String name;
    private String email;
    private String company;
    private String id;

   
    public Trainee() {
    }

   
    public Trainee(String name, String email, String company, String id) {
        this.name = name;
        this.email = email;
        this.company = company;
        this.id = id;
    }

}